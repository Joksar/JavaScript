function sum(arg_1, arg_2) {
    return arg_1 + arg_2;
}

function sub(arg_1, arg_2) {
    return arg_1 - arg_2;
}

function mult(arg_1, arg_2) {
    return arg_1 * arg_2;
}

function div(arg_1, arg_2) {
    return arg_1 / arg_2;
}

console.log(sum(11, 34));
console.log(sub(11, 34));
console.log(mult(11, 34));
console.log(div(11, 34));